void Map::TestLoadMap(int arr[16][21]) {
    for (unsigned row = 0; row < 16; ++row) {
        for (unsigned column = 0; column < 21; ++column) {
            map[row][column] = arr[row][column];
        }
    }
}

void Map::LoadMap(MinGL* window, vector<unique_ptr<GameObject>>& gameColliders) {
    unsigned type = 0;

    for (unsigned row = 0; row < 16; ++row) {
        for (unsigned column = 0; column < 21; ++column) {
            type = map[row][column];

            switch (type) {
                case 1:
                    wall = new GameObject("../assets/wall.si2", window, column * 40, row * 40);
                    gameColliders.emplace_back(wall);
                    break;
                default:
                    break;
            }
        }
    }
}
